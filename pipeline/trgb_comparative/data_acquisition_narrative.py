def write_data_acquisition_narrative(
    loader,
    out_path: Path,
    log_fn: Optional[Callable[[str], None]] = None,
) -> Path:
    """Render the peer-review-ready data acquisition narrative to disk.

    Parameters
    ----------
    loader : DataLoader
        Pipeline-wide DataLoader instance. Live SHA-256 / file metadata
        come from this; missing datasets cause that dataset's section
        to render a `_(unavailable)_` placeholder instead of aborting.
    out_path : Path
        Output filename, e.g.
        ``results/trgb_comparative/reports/data_acquisition_narrative.md``.
    log_fn : callable, optional
        Logger callback for progress messages.
    """
    log = log_fn or (lambda m: logger.info(m))
    out_path = Path(out_path)
    out_path.parent.mkdir(parents=True, exist_ok=True)

    parts = [
        "# Data acquisition narrative — TRGB comparative pipeline\n\n",
        f"*Generated {_now()} by `pipeline.trgb_comparative.data_acquisition_narrative`. "
        "SHA-256 values and file counts reflect the contents of `trgb_data/catalogs/` "
        "and `downloaded_data/` at generation time.*\n\n",
        _section_overview(),
        _section_anchors(loader),
        _section_trgb_tables(loader),
        _section_sn_samples(loader),
        _section_hoyt(loader),
        _section_auxiliary(),
        _section_audit_summary(),
        _section_reproducibility(),
        "## 9. References\n\n"
        "Full BibTeX records for every reference cited above are tabulated "
        "in [`data_tables.tex`](data_tables.tex) §6. The DOI / arXiv "
        "identifiers in this narrative resolve to the same papers and "
        "can be cross-referenced against that bibliography.\n",
    ]

    out_path.write_text("".join(parts))
    log(f"Data acquisition narrative → {out_path}")
    return out_path


__all__ = ["write_data_acquisition_narrative"]
