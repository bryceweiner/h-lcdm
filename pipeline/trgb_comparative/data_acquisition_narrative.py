"""Peer-review-ready data acquisition narrative for the TRGB comparative pipeline.

Produces a single ``data_acquisition_narrative.md`` in the pipeline's
reports directory documenting how every astronomical dataset used in
the analysis was obtained: source URLs, transcription protocol,
SHA-256 file manifests, spot-check verifications, calibrator-coverage
audit results, and reproducibility instructions for an external
reviewer to retrieve the data deterministically.

The narrative is regenerated by the standard pipeline run (called from
:py:meth:`TRGBComparativePipeline.run`) so SHA-256 values and file
counts always reflect the current contents of ``trgb_data/catalogs/`` and
``downloaded_data/``.
"""

from __future__ import annotations

