    def _void_cross_validation(self) -> Dict[str, Any]:
        """Perform cross-validation across void surveys."""
        try:
            void_data = self.results.get('void_data', {})
            catalog = void_data.get('catalog')

            # Validate catalog type
            if catalog is None:
                return {
                    'passed': True,  # Not applicable
                    'test': 'cross_validation',
                    'note': 'Cross-validation not applicable (no catalog available)'
                }
            
            if not isinstance(catalog, pd.DataFrame):
                return {
                    'passed': False,
                    'test': 'cross_validation',
                    'error': f'Catalog must be a DataFrame, got {type(catalog).__name__}'
                }

            if 'survey' not in catalog.columns:
                return {
                    'passed': True,  # Not applicable
                    'test': 'cross_validation',
                    'note': 'Cross-validation not applicable (single survey or no survey info)'
                }

            # Compare results across surveys
            survey_groups = catalog.groupby('survey')

            survey_results = {}
            for survey_name, survey_data in survey_groups:
                # Calculate basic statistics for this survey
                n_voids = len(survey_data)

                if 'orientation_deg' in survey_data.columns:
                    orientation_std = survey_data['orientation_deg'].std()
                    survey_results[survey_name] = {
                        'n_voids': n_voids,
                        'orientation_std': orientation_std
                    }
                else:
                    survey_results[survey_name] = {'n_voids': n_voids}

            # Check consistency across surveys
            if len(survey_results) > 1:
                # Simple consistency check: similar number of voids per survey
                void_counts = [r['n_voids'] for r in survey_results.values()]
                count_std = np.std(void_counts)
                count_mean = np.mean(void_counts)
                cv = count_std / count_mean if count_mean > 0 else 0

                # Surveys are consistent if coefficient of variation < 0.5
                consistent = cv < 0.5

                return {
                    'passed': consistent,
                    'test': 'survey_cross_validation',
                    'survey_results': survey_results,
                    'void_count_cv': cv,
                    'consistency_threshold': 0.5
                }
            else:
                return {
                    'passed': True,
                    'test': 'cross_validation',
                    'note': 'Only one survey available'
                }
        except Exception as e:
            return {
                'passed': False,
                'test': 'cross_validation',
                'error': str(e)
            }

    def _estimate_observed_survey_volume(self, obs_catalog: pd.DataFrame) -> float:
        """
        Estimate the effective survey volume for observed voids.

        Uses convex hull of void positions to estimate surveyed volume.
        """
        from scipy.spatial import ConvexHull

        # Ensure we have coordinate columns
        if not all(col in obs_catalog.columns for col in ['x', 'y', 'z']):
            raise ValueError(f"Observed catalog missing coordinate columns. Has: {list(obs_catalog.columns)}")
        
        positions = obs_catalog[['x', 'y', 'z']].values
        
        # Check for NaN/invalid coordinates
        valid_mask = np.isfinite(positions).all(axis=1)
        n_valid = valid_mask.sum()
        
        if n_valid == 0:
            raise ValueError(f"All {len(positions)} observed voids have invalid coordinates!")
        
        if n_valid < len(positions):
            self.logger.warning(f"Observed catalog has {len(positions) - n_valid} voids with invalid coordinates, using {n_valid} valid ones")
            positions = positions[valid_mask]
        
        try:
            hull = ConvexHull(positions)
            volume_mpc3 = hull.volume
            self.logger.info(f"Estimated observed survey volume from {len(positions)} voids: {volume_mpc3:.1e} Mpc³")
            return volume_mpc3
        except Exception as e:
            # Fallback: use bounding box if convex hull fails
            bbox_volume = (
                (positions[:, 0].max() - positions[:, 0].min()) *
                (positions[:, 1].max() - positions[:, 1].min()) *
                (positions[:, 2].max() - positions[:, 2].min())
            )
            self.logger.warning(f"Convex hull failed ({e}), using bounding box volume: {bbox_volume:.1e} Mpc³")
            return bbox_volume

    def _estimate_simulation_survey_volume(self, sim_catalog: pd.DataFrame) -> float:
        """
        Estimate the effective survey volume for simulation voids.

        Simulation is typically from a periodic box, but we use the same
        convex hull method for consistency with observed catalog.
        """
        return self._estimate_observed_survey_volume(sim_catalog)
