    def _estimate_chunk_memory_usage(self, n_nodes: int, chunk_size: int) -> float:
        """
        Estimate memory usage for chunked GPU clustering computation.

        Parameters:
            n_nodes: Total number of nodes
            chunk_size: Size of each chunk

        Returns:
            Estimated memory usage in MB
        """
        # Memory calculation for the main tensor operation: chunk_i x chunk_j x 3
        # In chunked processing, we use chunk_i = chunk_j = chunk_size
        # So peak memory is approximately: chunk_size * chunk_size * 3 * 4 bytes (float32)
        bytes_per_tensor = chunk_size * chunk_size * 3 * 4  # float32 = 4 bytes

        # Account for intermediate operations (factor of ~2-3)
        total_bytes = bytes_per_tensor * 3

        # Convert to MB
        total_mb = total_bytes / (1024 * 1024)

        return total_mb

    def _validate_chunk_size_for_gpu(self, n_nodes: int, chunk_size: int, max_memory_mb: float = 10240.0) -> bool:
        """
        Validate that chunk size won't exceed GPU memory limits.

        Parameters:
            n_nodes: Total number of nodes
            chunk_size: Size of each chunk
            max_memory_mb: Maximum allowed memory usage in MB

        Returns:
            True if chunk size is safe for GPU processing
        """
        if not self.use_gpu or not TORCH_AVAILABLE:
            return True  # CPU has no memory limits for this calculation

        estimated_mb = self._estimate_chunk_memory_usage(n_nodes, chunk_size)

        if estimated_mb > max_memory_mb:
            self.log_progress(f"Chunk size {chunk_size} would use ~{estimated_mb:.1f}MB, exceeding limit of {max_memory_mb:.1f}MB")
            return False

        return True

    def _chunked_null_hypothesis_random_networks(self, n_simulations: int, random_seed: int = 42,
                                                chunk_size: int = 18000) -> Dict[str, Any]:
        """
        Perform chunked null hypothesis testing with MPS-accelerated random void networks.

        Divides the void catalog into chunks to avoid memory overflow while using MPS acceleration.
        Each chunk is processed independently with random void positions.

        Parameters:
            n_simulations: Number of random network simulations
            random_seed: Random seed for reproducibility
            chunk_size: Maximum voids per chunk for MPS processing (default: 18000 for ~10GB)

        Returns:
            dict: Null hypothesis test results
        """
        try:
            void_data = self.results.get('void_data', {})
            catalog = void_data.get('catalog')

            # If catalog is a string (from JSON serialization), reload from pickle
            if isinstance(catalog, str) or catalog is None:
                catalog_path = Path('processed_data') / 'voids_deduplicated.pkl'
                if catalog_path.exists():
                    import pickle
                    with open(catalog_path, 'rb') as f:
                        catalog = pickle.load(f)
                    # Apply quality cuts
                    if 'radius_mpc' in catalog.columns:
                        catalog = catalog[catalog['radius_mpc'] >= 5.0]
                    if 'redshift' in catalog.columns:
                        catalog = catalog[(catalog['redshift'] > 0.005) & (catalog['redshift'] < 1.2)]
                else:
                    return {'passed': False, 'error': 'No void catalog available'}

            if catalog is None or (hasattr(catalog, 'empty') and catalog.empty):
                return {'passed': False, 'error': 'No void catalog available'}

            # Get observed clustering coefficient
            clustering_results = self.results.get('clustering_analysis', {})
            observed_cc = clustering_results.get('observed_clustering_coefficient', 0.0)

            # Get spatial boundaries and size distribution from actual catalog
            n_voids = len(catalog)

            # Extract spatial bounds - handle both column naming conventions
            ra_col = 'ra' if 'ra' in catalog.columns else 'ra_deg'
            dec_col = 'dec' if 'dec' in catalog.columns else 'dec_deg'
            ra_min, ra_max = catalog[ra_col].min(), catalog[ra_col].max()
            dec_min, dec_max = catalog[dec_col].min(), catalog[dec_col].max()
            z_min, z_max = catalog['redshift'].min(), catalog['redshift'].max()

            # Extract size distribution - handle different column names
            if 'radius_mpc' in catalog.columns:
                size_dist = catalog['radius_mpc'].values
            elif 'reff' in catalog.columns:
                size_dist = catalog['reff'].values
            elif 'radius' in catalog.columns:
                size_dist = catalog['radius'].values
            else:
                size_dist = np.full(n_voids, 20.0)  # Default

            # Calculate linking length from size distribution (pre-compute once)
            mean_reff = np.nanmean(size_dist)
            linking_length = 3.0 * mean_reff

            # Determine optimal chunk size based on available compute and memory limits
            if self.use_gpu and TORCH_AVAILABLE:
                # Use large chunks for MPS to maximize performance (up to 1GB+ memory)
                max_chunk_size = min(chunk_size, 8000)  # Allow much larger chunks
                # Validate chunk size against memory limits
                while max_chunk_size > 100 and not self._validate_chunk_size_for_gpu(n_voids, max_chunk_size):
                    max_chunk_size -= 200  # Decrease in larger steps
                chunk_size = max_chunk_size
                device_name = f"MPS-chunked ({chunk_size} voids/chunk)"
            else:
                # Use larger chunks for CPU
                chunk_size = min(chunk_size, 10000)
                device_name = f"CPU-chunked ({chunk_size} voids/chunk)"

            # Calculate number of chunks needed
            n_chunks = int(np.ceil(n_voids / chunk_size))
            voids_per_chunk = [chunk_size] * (n_chunks - 1) + [n_voids - (n_chunks - 1) * chunk_size]

            # Estimate performance improvement
            estimated_memory_mb = self._estimate_chunk_memory_usage(n_voids, chunk_size)
            self.log_progress(f"Running {n_simulations} null hypothesis simulations on {device_name}")
            self.log_progress(f"Processing {n_voids} voids in {n_chunks} chunks: {voids_per_chunk}")
            self.log_progress(f"Estimated chunk memory usage: {estimated_memory_mb:.1f}MB per chunk")

            # Pre-compute coordinate conversion setup
            from astropy import units as u
            from astropy.coordinates import SkyCoord
            from astropy.cosmology import Planck18 as cosmo

            # Generate random void networks in chunks
            random_ccs = []
            np.random.seed(random_seed)

            start_time = time.time()

            for sim_i in tqdm(range(n_simulations), desc="Null hypothesis", unit="sim"):
                # Generate random void positions for entire catalog (Poisson process)
                random_ra = np.random.uniform(ra_min, ra_max, n_voids)
                random_dec = np.random.uniform(dec_min, dec_max, n_voids)
                random_z = np.random.uniform(z_min, z_max, n_voids)

                # Convert to Cartesian coordinates (do this once per simulation)
                coords = SkyCoord(
                    ra=random_ra * u.deg,
                    dec=random_dec * u.deg,
                    distance=cosmo.comoving_distance(random_z)
                )
                cart_coords = coords.cartesian
                all_positions = np.column_stack([
                    cart_coords.x.value,
                    cart_coords.y.value,
                    cart_coords.z.value
                ])

                # Validate positions
                if np.any(~np.isfinite(all_positions)):
                    # Skip this simulation if coordinates are invalid
                    continue

                # Process in chunks to accumulate clustering coefficient
                chunk_ccs = []

                for chunk_i, chunk_voids in enumerate(voids_per_chunk):
                    start_idx = sum(voids_per_chunk[:chunk_i])
                    end_idx = start_idx + chunk_voids

                    # Extract positions for this chunk
                    chunk_positions = all_positions[start_idx:end_idx]

                    # Calculate clustering coefficient for this chunk
                    try:
                        if self.use_gpu and TORCH_AVAILABLE:
                            chunk_cc = self._calculate_clustering_coefficient_gpu(chunk_positions, linking_length)
                        else:
                            chunk_cc = self._calculate_clustering_coefficient_cpu(chunk_positions, linking_length)
                        chunk_ccs.append(chunk_cc)
                    except Exception as e:
                        # If chunk fails, use CPU fallback
                        self.log_progress(f"Chunk {chunk_i} failed ({e}), using CPU fallback")
                        chunk_cc = self._calculate_clustering_coefficient_cpu(chunk_positions, linking_length)
                        chunk_ccs.append(chunk_cc)

                # Combine chunk results: weighted average by number of voids in each chunk
                if chunk_ccs:
                    weights = np.array(voids_per_chunk) / n_voids
                    combined_cc = np.average(chunk_ccs, weights=weights)
                    random_ccs.append(combined_cc)

            elapsed = time.time() - start_time
            self.log_progress(f"Null hypothesis testing completed in {elapsed:.1f}s ({elapsed/n_simulations:.2f}s per simulation)")

            if not random_ccs:
                return {
                    'passed': False,
                    'test': 'chunked_null_hypothesis_random_networks',
                    'error': 'No valid simulations completed'
                }

            random_ccs = np.array(random_ccs)

            # Calculate p-value: probability that random network has clustering >= observed
            p_value = np.mean(random_ccs >= observed_cc)

            # Calculate significance in sigma
            random_mean = np.mean(random_ccs)
            random_std = np.std(random_ccs)
            sigma = (observed_cc - random_mean) / random_std if random_std > 0 else 999.0

            # Reject null hypothesis if p < 0.05 (or sigma > 2)
            reject_null = p_value < 0.05 or sigma > 2.0

            return {
                'passed': reject_null,
                'test': 'chunked_null_hypothesis_random_networks',
                'n_simulations': n_simulations,
                'chunk_size': chunk_size,
                'n_chunks': n_chunks,
                'observed_clustering_coefficient': observed_cc,
                'random_mean': float(random_mean),
                'random_std': float(random_std),
                'p_value': float(p_value),
                'sigma': float(sigma),
                'interpretation': f'Chunked random network null hypothesis {"rejected" if reject_null else "not rejected"} at {sigma:.1f}σ (p = {p_value:.5f})'
            }

        except Exception as e:
            return {
                'passed': False,
                'test': 'chunked_null_hypothesis_random_networks',
                'error': str(e)
            }

    def _chunked_bootstrap_clustering_validation(self, n_bootstrap: int, random_seed: int = 42,
                                                chunk_size: int = 18000) -> Dict[str, Any]:
        """
        Perform chunked bootstrap validation of clustering coefficient with MPS acceleration.

        Uses chunked processing to handle large catalogs efficiently while maintaining
        statistical validity of bootstrap resampling.

        Parameters:
            n_bootstrap: Number of bootstrap iterations
            random_seed: Random seed for reproducibility
            chunk_size: Maximum voids per chunk for processing (default: 18000 for ~10GB)

        Returns:
            dict: Bootstrap validation results
        """
        try:
            void_data = self.results.get('void_data', {})
            catalog = void_data.get('catalog')

            # If catalog is a string or dict (from JSON serialization), reload from pickle
            if isinstance(catalog, (str, dict)) or catalog is None:
                catalog_path = Path('processed_data') / 'voids_deduplicated.pkl'
                if catalog_path.exists():
                    import pickle
                    with open(catalog_path, 'rb') as f:
                        catalog = pickle.load(f)
                    # Apply quality cuts
                    if 'radius_mpc' in catalog.columns:
                        catalog = catalog[catalog['radius_mpc'] >= 5.0]
                    if 'redshift' in catalog.columns:
                        catalog = catalog[(catalog['redshift'] > 0.005) & (catalog['redshift'] < 1.2)]
                else:
                    return {
                        'passed': False,
                        'test': 'chunked_bootstrap_clustering_validation',
                        'error': 'No void catalog available',
                        'n_bootstrap': n_bootstrap
                    }

            if catalog is None or (hasattr(catalog, 'empty') and catalog.empty):
                return {
                    'passed': False,
                    'test': 'chunked_bootstrap_clustering_validation',
                    'error': 'No void catalog available',
                    'n_bootstrap': n_bootstrap
                }

            # Get observed clustering coefficient - check if analysis actually succeeded
            clustering_results = self.results.get('clustering_analysis', {})
            if 'error' in clustering_results:
                return {
                    'passed': False,
                    'test': 'chunked_bootstrap_clustering_validation',
                    'error': f"Clustering analysis failed: {clustering_results.get('error')}",
                    'n_bootstrap': n_bootstrap
                }
            observed_cc = clustering_results.get('observed_clustering_coefficient', None)
            if observed_cc is None:
                return {
                    'passed': False,
                    'test': 'chunked_bootstrap_clustering_validation',
                    'error': 'No observed clustering coefficient available',
                    'n_bootstrap': n_bootstrap
                }

            # Pre-compute positions and linking length for all bootstrap iterations
            from pipeline.common.void_coordinates import get_cartesian_positions
            from pipeline.common.void_stats import calculate_robust_linking_length

            # Get positions once and filter invalid ones
            positions, _ = get_cartesian_positions(catalog)
            if positions is None or len(positions) == 0:
                return {
                    'passed': False,
                    'test': 'chunked_bootstrap_clustering_validation',
                    'error': 'Could not extract coordinates from catalog',
                    'n_bootstrap': n_bootstrap
                }

            # Filter invalid positions
            valid_mask = np.isfinite(positions).all(axis=1)
            positions = positions[valid_mask]
            catalog_valid = catalog[valid_mask].copy()

            if len(catalog_valid) == 0:
                return {
                    'passed': False,
                    'test': 'chunked_bootstrap_clustering_validation',
                    'error': 'No valid coordinates after filtering',
                    'n_bootstrap': n_bootstrap
                }

            # Ensure catalog has x, y, z for linking length calculation
            if not all(col in catalog_valid.columns for col in ['x', 'y', 'z']):
                catalog_valid['x'] = positions[:, 0]
                catalog_valid['y'] = positions[:, 1]
                catalog_valid['z'] = positions[:, 2]

            # Calculate linking length once
            linking_length, _ = calculate_robust_linking_length(catalog_valid, method='robust')

            # Determine optimal chunk size
            n_total = len(positions)
            if self.use_gpu and TORCH_AVAILABLE:
                # Use large chunks for MPS to maximize performance (up to 1GB+ memory)
                max_chunk_size = min(chunk_size, 8000)  # Allow much larger chunks
                # Validate chunk size against memory limits
                while max_chunk_size > 100 and not self._validate_chunk_size_for_gpu(n_total, max_chunk_size):
                    max_chunk_size -= 200  # Decrease in larger steps
                chunk_size = max_chunk_size
                device_name = f"MPS-chunked ({chunk_size} voids/chunk)"
            else:
                chunk_size = min(chunk_size, 10000)
                device_name = f"CPU-chunked ({chunk_size} voids/chunk)"

            # Calculate number of chunks
            n_chunks = int(np.ceil(n_total / chunk_size))
            voids_per_chunk = [chunk_size] * (n_chunks - 1) + [n_total - (n_chunks - 1) * chunk_size]

            # Estimate performance improvement
            estimated_memory_mb = self._estimate_chunk_memory_usage(n_total, chunk_size)
            self.log_progress(f"Running {n_bootstrap} bootstrap iterations on {device_name}")
            self.log_progress(f"Processing {n_total} voids in {n_chunks} chunks: {voids_per_chunk}")
            self.log_progress(f"Estimated chunk memory usage: {estimated_memory_mb:.1f}MB per chunk")

            # Generate bootstrap samples
            bootstrap_ccs = []
            np.random.seed(random_seed)

            start_time = time.time()

            for boot_i in tqdm(range(n_bootstrap), desc="Bootstrap", unit="iter"):
                # Resample the entire catalog indices with replacement
                bootstrap_indices = np.random.choice(n_total, size=n_total, replace=True)
                bootstrap_positions = positions[bootstrap_indices]

                # Process bootstrap sample in chunks
                chunk_ccs = []

                for chunk_i, chunk_voids in enumerate(voids_per_chunk):
                    start_idx = sum(voids_per_chunk[:chunk_i])
                    end_idx = start_idx + chunk_voids

                    # Extract positions for this chunk
                    chunk_positions = bootstrap_positions[start_idx:end_idx]

                    # Calculate clustering coefficient for this chunk
                    try:
                        if self.use_gpu and TORCH_AVAILABLE:
                            chunk_cc = self._calculate_clustering_coefficient_gpu(chunk_positions, linking_length)
                        else:
                            chunk_cc = self._calculate_clustering_coefficient_cpu(chunk_positions, linking_length)
                        chunk_ccs.append(chunk_cc)
                    except Exception as e:
                        # If chunk fails, use CPU fallback
                        self.log_progress(f"Bootstrap {boot_i}, chunk {chunk_i} failed ({e}), using CPU fallback")
                        chunk_cc = self._calculate_clustering_coefficient_cpu(chunk_positions, linking_length)
                        chunk_ccs.append(chunk_cc)

                # Combine chunk results: weighted average by number of voids in each chunk
                if chunk_ccs:
                    weights = np.array(voids_per_chunk) / n_total
                    combined_cc = np.average(chunk_ccs, weights=weights)
                    bootstrap_ccs.append(combined_cc)

            elapsed = time.time() - start_time
            self.log_progress(f"Bootstrap completed in {elapsed:.1f}s ({elapsed/n_bootstrap:.2f}s per iteration)")

            if not bootstrap_ccs:
                return {
                    'passed': False,
                    'test': 'chunked_bootstrap_clustering_validation',
                    'error': 'No valid bootstrap iterations completed',
                    'n_bootstrap': n_bootstrap
                }

            bootstrap_ccs = np.array(bootstrap_ccs)

            # Analyze bootstrap distribution
            bootstrap_mean = np.mean(bootstrap_ccs)
            bootstrap_std = np.std(bootstrap_ccs)

            # Calculate z-score: how many sigma is bootstrap mean from observed?
            z_score = abs(bootstrap_mean - observed_cc) / bootstrap_std if bootstrap_std > 0 else 0.0

            # Check stability: bootstrap mean should be very close to observed (z < 0.1)
            stable = z_score < 0.1

            # Calculate confidence intervals
            ci_68 = np.percentile(bootstrap_ccs, [16, 84])
            ci_95 = np.percentile(bootstrap_ccs, [2.5, 97.5])

            # Compare bootstrap distribution to reference values
            # Note: These comparisons test different hypotheses
            
            # Get network stats from clustering results
            n_edges = clustering_results.get('network_edges', 0)
            n_total = len(catalog_valid)
            
            # 1. Geometric null hypothesis (spatially random voids)
            c_random = (2.0 * n_edges) / (n_total * (n_total - 1)) if n_total > 1 else 0.0
            
            # 2. ΛCDM galaxy networks (from simulations, MNRAS 495, 1311, 2020)
            # Galaxy networks at linking lengths 3.5-8.1 h^-1 Mpc show C = 0.41-0.56
            # Taking mid-range as comparison point
            c_lcdm_galaxy = 0.51  # Mid-range of observed galaxy network values
            c_lcdm_range = (0.41, 0.56)  # Full range from literature
            
            # 3. H-ΛCDM theory predictions (if applicable)
            eta_natural = 0.4430  # (1 - ln(2)) / ln(2) ≈ 0.443 (thermodynamic efficiency)
            c_e8 = 0.78125  # 25/32, E8×E8 pure substrate

            # Calculate statistical distances
            random_sigma = abs(bootstrap_mean - c_random) / bootstrap_std if bootstrap_std > 0 else 999.0
            lcdm_sigma = abs(bootstrap_mean - c_lcdm_galaxy) / bootstrap_std if bootstrap_std > 0 else 999.0
            eta_natural_sigma = abs(bootstrap_mean - eta_natural) / bootstrap_std if bootstrap_std > 0 else 999.0
            c_e8_sigma = abs(bootstrap_mean - c_e8) / bootstrap_std if bootstrap_std > 0 else 999.0

            return {
                'passed': stable,
                'test': 'chunked_bootstrap_clustering_validation',
                'n_bootstrap': n_bootstrap,
                'chunk_size': chunk_size,
                'n_chunks': n_chunks,
                'observed_clustering_coefficient': observed_cc,
                'bootstrap_mean': float(bootstrap_mean),
                'bootstrap_std': float(bootstrap_std),
                'z_score': float(z_score),
                'ci_68': ci_68.tolist(),
                'ci_95': ci_95.tolist(),
                'comparison_to_models': {
                    'geometric_null': {
                        'value': float(c_random),
                        'sigma': float(random_sigma),
                        'within_ci_95': ci_95[0] <= c_random <= ci_95[1],
                        'description': 'Spatially random void positions (Erdős-Rényi)'
                    },
                    'lcdm_galaxy_networks': {
                        'value': float(c_lcdm_galaxy),
                        'range': c_lcdm_range,
                        'sigma': float(lcdm_sigma),
                        'within_ci_95': ci_95[0] <= c_lcdm_galaxy <= ci_95[1],
                        'within_range': c_lcdm_range[0] <= bootstrap_mean <= c_lcdm_range[1],
                        'description': 'ΛCDM galaxy networks from simulations (MNRAS 495, 1311, 2020)',
                        'note': 'Same methodology, opposite density phase'
                    },
                    'thermodynamic_efficiency': {
                        'value': eta_natural,
                        'sigma': float(eta_natural_sigma),
                        'within_ci_95': ci_95[0] <= eta_natural <= ci_95[1],
                        'description': 'H-ΛCDM thermodynamic ratio'
                    },
                    'e8_pure_substrate': {
                        'value': c_e8,
                        'sigma': float(c_e8_sigma),
                        'within_ci_95': ci_95[0] <= c_e8 <= ci_95[1],
                        'description': 'H-ΛCDM E8×E8 substrate'
                    }
                },
                'interpretation': f'Bootstrap mean {bootstrap_mean:.4f} ± {bootstrap_std:.4f} is {"stable" if stable else "unstable"} (z = {z_score:.2f}σ). '
                                f'Geometric null: {random_sigma:.1f}σ (strongly rejected). '
                                f'ΛCDM galaxy networks: {lcdm_sigma:.1f}σ ({"consistent" if lcdm_sigma < 2.0 else "different"}). '
                                f'{"Within ΛCDM galaxy network range." if c_lcdm_range[0] <= bootstrap_mean <= c_lcdm_range[1] else "Outside ΛCDM galaxy network range."}'
            }

        except Exception as e:
            return {
                'passed': False,
                'test': 'chunked_bootstrap_clustering_validation',
                'error': str(e)
            }

    def _lcdm_simulation_comparison(
        self,
        obs_results: Dict[str, Any],
        n_bootstrap: int = 1000,
        random_seed: int = 42
    ) -> Dict[str, Any]:
        """
        Direct comparison to Quijote ΛCDM simulation void networks.

        Statistical tests:
        1. Point estimate z-score: |C_obs - C_sim| / σ_combined
        2. Bootstrap distribution overlap (95% CI)
        3. Kolmogorov-Smirnov test on local clustering distributions
        4. Permutation test for systematic differences

        Returns:
            passed: bool (consistency within 2σ)
            c_obs: Observed clustering coefficient
            c_sim: Simulation clustering coefficient
            z_score: Statistical significance
            ci_overlap: Confidence interval overlap fraction
            ks_p_value: K-S test p-value
            interpretation: Text summary
        """
        from scipy.stats import ks_2samp
        import networkx as nx
        import pickle

        self.logger.info("=" * 60)
        self.logger.info("ΛCDM SIMULATION COMPARISON (Quijote Gigantes)")
        self.logger.info("=" * 60)

        # Load observational results
        obs_catalog = obs_results['void_data']['catalog']
        obs_clustering = obs_results['clustering_analysis']
        obs_cc = obs_clustering['observed_clustering_coefficient']
        obs_std = obs_clustering['observed_clustering_std']
        
        # DEBUG: Check what columns obs_catalog has
        self.logger.info(f"DEBUG: obs_catalog has {len(obs_catalog)} rows and columns: {list(obs_catalog.columns)[:10]}...")
        if 'x' in obs_catalog.columns:
            n_valid_x = obs_catalog['x'].notna().sum()
            self.logger.info(f"DEBUG: obs_catalog has 'x' column with {n_valid_x}/{len(obs_catalog)} non-NaN values")
        else:
            self.logger.error("DEBUG: obs_catalog does NOT have 'x' column!")

        # Check for cached ΛCDM network graph
        cache_dir = self.processed_data_dir
        lcdm_graph_cache = cache_dir / "lcdm_void_network_graph.pkl"
        lcdm_metadata_cache = cache_dir / "lcdm_void_network_metadata.pkl"

        # Cache versioning - invalidate when subsampling is added
        current_cache_version = "resolution_matched_v1"

        sim_catalog = None
        sim_clustering = None

        if lcdm_graph_cache.exists() and lcdm_metadata_cache.exists():
            try:
                self.logger.info("Found cached ΛCDM network graph, loading...")
                with open(lcdm_graph_cache, 'rb') as f:
                    cached_graph = pickle.load(f)
                with open(lcdm_metadata_cache, 'rb') as f:
                    cached_metadata = pickle.load(f)

                # Check cache version
                cache_version = cached_metadata.get('cache_version', 'legacy')
                if cache_version != current_cache_version:
                    self.logger.info(f"  Cache version mismatch ({cache_version} vs {current_cache_version}), will rebuild network")
                    sim_clustering = None
                elif isinstance(cached_graph, nx.Graph) and cached_graph.number_of_nodes() > 0:
                    self.logger.info(f"  ✓ Loaded cached ΛCDM network: {cached_graph.number_of_nodes():,} nodes, {cached_graph.number_of_edges():,} edges")

                    # Reconstruct sim_clustering from cached data
                    sim_clustering = {
                        'graph': cached_graph,
                        'clustering_coefficient': cached_metadata['clustering_coefficient'],
                        'clustering_std': cached_metadata['clustering_std'],
                        'n_nodes': cached_metadata['n_nodes'],
                        'n_edges': cached_metadata['n_edges'],
                        'linking_length': cached_metadata['linking_length']
                    }
                    sim_catalog_full = cached_metadata.get('catalog')

                    # Apply subsampling to cached data for resolution matching
                    if sim_catalog_full is not None:
                        obs_volume = self._estimate_observed_survey_volume(obs_catalog)
                        sim_volume = self._estimate_simulation_survey_volume(sim_catalog_full)

                        sim_catalog = self.data_processor.subsample_to_match_density(
                            sim_catalog_full,
                            obs_catalog,
                            obs_volume,
                            sim_volume,
                            random_seed=random_seed
                        )

                        # Update clustering results for subsampled catalog
                        if len(sim_catalog) != len(sim_catalog_full):
                            self.logger.info("  Recomputing network analysis for subsampled cached catalog...")
                            from pipeline.common.void_network import build_void_network
                            subsampled_network = build_void_network(
                                sim_catalog,
                                linking_length=sim_clustering['linking_length']
                            )
                            sim_clustering = subsampled_network
                            sim_clustering['subsampling_info'] = {
                                'original_voids': len(sim_catalog_full),
                                'subsampled_voids': len(sim_catalog),
                                'subsampling_ratio': len(sim_catalog) / len(sim_catalog_full)
                            }

                        self.logger.info(f"  ✓ Using cached ΛCDM clustering: C = {sim_clustering['clustering_coefficient']:.4f}")
                    else:
                        sim_catalog = sim_catalog_full
                else:
                    self.logger.warning("  Cache validation failed, will rebuild network")
                    sim_clustering = None
            except Exception as e:
                self.logger.warning(f"  Failed to load cached ΛCDM network: {e}")
                self.logger.info("  Will rebuild network from scratch")
                sim_clustering = None
        
        # If no valid cache, process simulation voids
        if sim_clustering is None:
            # Check for cached processed simulation catalog (before network building)
            sim_processed_cache = cache_dir / "quijote_gigantes_processed.pkl"
            sim_catalog_full = None
            
            if sim_processed_cache.exists():
                try:
                    self.logger.info("Found cached processed simulation catalog, loading...")
                    import pickle
                    with open(sim_processed_cache, 'rb') as f:
                        cached_sim_data = pickle.load(f)
                    sim_catalog_full = cached_sim_data['catalog']
                    self.logger.info(f"  ✓ Loaded {len(sim_catalog_full):,} processed simulation voids from cache")
                except Exception as e:
                    self.logger.warning(f"  Failed to load cached simulation catalog: {e}")
                    sim_catalog_full = None
            
            # If no cached processed catalog, process from raw data
            if sim_catalog_full is None:
                # Download and process Quijote voids
                self.logger.info("Downloading Quijote Gigantes void catalog...")
                quijote_raw = self.data_processor.loader.download_quijote_gigantes_voids(
                    snapshot="z0",
                    cosmology="fiducial"
                )

                if quijote_raw is None or len(quijote_raw) == 0:
                    self.logger.error("=" * 70)
                    self.logger.error("FAILED TO OBTAIN QUIJOTE ΛCDM VOID CATALOG")
                    self.logger.error("=" * 70)
                    self.logger.error("")
                    self.logger.error("The Quijote simulation data is required for ΛCDM comparison.")
                    self.logger.error("Check the log above for Globus download instructions.")
                    self.logger.error("")
                    return {
                        'passed': False,
                        'error': 'Failed to download Quijote Gigantes voids - see Globus instructions above',
                        'test': 'lcdm_simulation_comparison'
                    }

                # Process simulation voids: quality cuts, deduplication, coordinate computation
                # BUT skip expensive network building until AFTER downsampling
                self.logger.info("Processing simulation voids (quality cuts + deduplication)...")
                sim_results = self.data_processor.process_simulation_void_catalog(
                    quijote_raw,
                    obs_catalog,
                    obs_processing_params={
                        'quality_cuts': {
                            'r_min': obs_catalog['radius_mpc'].min(),
                            'r_max': obs_catalog['radius_mpc'].max(),
                            'z_min': obs_catalog['redshift'].min(),
                            'z_max': obs_catalog['redshift'].max()
                        }
                        # linking_length not passed - simulation computes its own after downsampling
                    },
                    build_network=False  # Skip network building - do it after downsampling
                )

                sim_catalog_full = sim_results['catalog']
                
                # Cache the processed catalog for future runs
                self.logger.info(f"Caching processed simulation catalog ({len(sim_catalog_full):,} voids)...")
                with open(sim_processed_cache, 'wb') as f:
                    pickle.dump({'catalog': sim_catalog_full}, f)
                self.logger.info(f"  ✓ Cached to {sim_processed_cache}")
            
            # Downsample SIMULATION to match OBSERVED void density (observations unchanged)
            self.logger.info("\nDownsampling simulation to match observed void density...")
            
            # The obs_catalog from obs_results['void_data']['catalog'] doesn't have coordinates (they're NaN)
            # We need to load the persisted catalog from disk which has the computed coordinates
            self.logger.info("Loading persisted observed catalog with coordinates from disk...")
            persisted_catalog = self.data_processor.load_processed_data("void_catalogs_combined")
            
            if persisted_catalog is None or 'catalog' not in persisted_catalog:
                raise ValueError("Failed to load persisted observed catalog from processed_data/void_catalogs_combined_processed.pkl")
            
